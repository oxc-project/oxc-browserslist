pub fn get_usage_by_region(region: &str) -> Option<RegionData> {
    let index = keys().binary_search(&region).ok()?;
    Some(RegionData::new(
        PAIR_RANGES[index],
        PAIR_RANGES[index + 1],
        PERCENT_RANGES[index],
        PERCENT_RANGES[index + 1],
    ))
}
